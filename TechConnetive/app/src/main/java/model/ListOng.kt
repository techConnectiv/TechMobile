package model

class ListOng {

    var iconOng: Int? = 0
    var listOng: String? = null
    var descricao: String? = null
    var km: String? = null

    constructor(iconOng: Int?, listOng: String?, descricao: String?, km: String?) {
        this.iconOng = iconOng
        this.listOng = listOng
        this.descricao = descricao
        this.km = km
    }
}